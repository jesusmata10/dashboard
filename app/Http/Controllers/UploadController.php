        'file'  =>  'required|file|image|mimes:jpeg,png,gif,jpg|max:2048'
        ]);

        $file = $validation['file'];

        // Genera el nombre de archivo con extensiÃ³n
        $fileName = 'factura-'.time().'.'.$file->getClientOriginalExtension();

        // Guarda el archivo ('nombre de la carpeta', 'nombre del archivo')
        $path = $file->storeAs($fileName, $fileName);

